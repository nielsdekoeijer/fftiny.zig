const butterflies = @import("butterflies.zig");
const std = @import("std");
const testing = std.testing;

pub const FFTMODE = butterflies.FFTMODE;

pub fn plan(comptime T: type, comptime n: usize, comptime mode: FFTMODE) type {
    comptime @setFloatMode(.Optimized);
    std.debug.assert(@typeInfo(T) == .Float);
    std.debug.assert(n % 2 == 0);
    return struct {
        const Self = @This();
        const mask = n - 1;
        // index helper
        pub inline fn iidx(comptime i: usize, comptime input_offset: usize, comptime stride: usize) usize {
            return 2 * ((input_offset +% (i * stride)) & mask);
        }
        // user function
        pub fn transform(self: Self, x: *[n]std.math.Complex(T), y: *[n]std.math.Complex(T)) void {
            _ = self;
            recurse(0, 0, 1, n, @ptrCast(*[2 * n]T, x), @ptrCast(*[2 * n]T, y));
        }
        fn recurse(comptime input_offset: usize, comptime output_offset: usize, comptime stride: usize, comptime size: usize, x: *[2 * n]T, y: *[2 * n]T) void {
            switch (size) {
                1 => {
                    const iidx0 = comptime iidx(0, input_offset, stride);
                    const x0 = x.*[iidx0 .. iidx0 + 2];
                    y[2 * output_offset .. 2 * output_offset + 2].* = x0.*;
                },
                2 => {
                    const iidx0 = comptime iidx(0, input_offset, stride);
                    const x0 = x[iidx0 .. iidx0 + 2];
                    const iidx1 = comptime iidx(1, input_offset, stride);
                    const x1 = x[iidx1 .. iidx1 + 2];
                    y[2 * output_offset + 0 .. 2 * output_offset + 4].* = butterflies.butterfly2sp(T, mode, x0, x1);
                },
                4 => {
                    const iidx0 = comptime iidx(0, input_offset, stride);
                    const iidx1 = comptime iidx(1, input_offset, stride);
                    const x01 = x[iidx0 .. iidx0 + 2] ++ x[iidx1 .. iidx1 + 2];
                    const iidx2 = comptime iidx(2, input_offset, stride);
                    const iidx3 = comptime iidx(3, input_offset, stride);
                    const x23 = x[iidx2 .. iidx2 + 2] ++ x[iidx3 .. iidx3 + 2];

                    y[2 * output_offset + 0 .. 2 * output_offset + 8].* = butterflies.butterfly4sp(T, mode, x01, x23);
                },
                else => {
                    recurse(input_offset, output_offset, stride * 2, size / 2, x, y);
                    recurse(input_offset +% stride, output_offset + size / 2, stride * 4, size / 4, x, y);
                    recurse(input_offset -% stride, output_offset + 3 * size / 4, stride * 4, size / 4, x, y);

                    const kmax = size / 4;

                    const iidy0 = comptime 2 * (output_offset + 0 * size / 4);
                    const iidy1 = comptime 2 * (output_offset + 1 * size / 4);
                    const iidy2 = comptime 2 * (output_offset + 2 * size / 4);
                    const iidy3 = comptime 2 * (output_offset + 3 * size / 4);

                    const y0 = y[iidy0 .. iidy0 + 2 * (kmax - 1) + 2];
                    const y2 = y[iidy2 .. iidy2 + 2 * (kmax - 1) + 2];
                    const y1 = y[iidy1 .. iidy1 + 2 * (kmax - 1) + 2];
                    const y3 = y[iidy3 .. iidy3 + 2 * (kmax - 1) + 2];

                    @setEvalBranchQuota(10000000);
                    inline for (0..kmax) |k| {
                        butterflies.butterflySR4l(
                            T,
                            mode,
                            size / 4,
                            k,
                            y0[2 * k .. 2 * k + 2],
                            y2[2 * k .. 2 * k + 2],
                            y1[2 * k .. 2 * k + 2],
                            y3[2 * k .. 2 * k + 2],
                        );
                    }
                },
            }
        }

        pub fn init() Self {
            return Self{};
        }
    };
}

